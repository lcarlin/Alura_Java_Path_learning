
public class TestaCaracteres {
	public static void main(String [] args ) {
		char letra = 'a' ; 
		System.out.println(letra);
		
		char valor = 66 ; 
		System.out.println(valor ) ;
		
		valor = (char) (valor + 1) ;
		
		System.out.println(valor ) ;
		
		String palavra = "Teste de uma longa string em Java " ; 
		System.out.println( palavra ) ;
		int ano = 2020 ; 
		palavra = palavra + ano ;
		System.out.println( palavra ) ;
		
	}

}
